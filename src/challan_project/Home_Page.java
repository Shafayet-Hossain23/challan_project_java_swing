    private void show_table_data() {
        try {
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/swing_project", "root", "");
            Statement stmt = (Statement) con.createStatement();
            String sql = "SELECT * FROM challan_list ORDER BY challan_no DESC";
            ResultSet rs = stmt.executeQuery(sql);
            
            DefaultTableModel tblModel = (DefaultTableModel) jTable1.getModel();
            tblModel.setRowCount(0);
            while (rs.next()) {
                String challan_no = String.valueOf(rs.getInt("challan_no"));
                String challan_date = rs.getString("challan_date");
                String po_no = rs.getString("po_no");
                String po_date = rs.getString("po_date");
                String product_code = rs.getString("product_code");
                String product_name = rs.getString("product_name");
                String company_name = rs.getString("company_name");
                String company_location = rs.getString("company_location");
                String product_quantity = String.valueOf(rs.getInt("product_quantity"));
                String packet_size = String.valueOf(rs.getInt("packet_size"));
                
                String tbData[] = {challan_no, challan_date, po_no, po_date, product_code, product_name, company_name, company_location, product_quantity, packet_size};
                
                tblModel.addRow(tbData);
                
            }
            
            con.close();
            
        } catch (Exception e) {
            System.out.println(e.getMessage());
        }
        
    }
    
