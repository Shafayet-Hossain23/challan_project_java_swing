    private void edit_productActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_edit_productActionPerformed
        // TODO add your handling code here:

        String pro_co = (String) product_table.getValueAt(product_table.getSelectedRow(), 0);
        String pro_code = p_code.getText();
        
        String pro_name = p_name.getText();
        
        String com_name = c_name.getText();
        
        String com_location = c_location.getText();
        
        if (pro_code.isEmpty() || pro_name.isEmpty() || com_name.isEmpty() || com_location.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please fill up the all field");
        } else {
            
            try {
                Class.forName("com.mysql.jdbc.Driver");
                Connection con = (Connection) DriverManager.getConnection("jdbc:mysql://localhost:3306/swing_project", "root", "");
                Statement stmt = (Statement) con.createStatement();
                String sql = "update product_details set product_code = '" + pro_code + "', product_name = '" + pro_name + "', company_name = '" + com_name + "', company_location = '" + com_location + "'  where product_code='" + pro_co + "'";
                boolean b = stmt.execute(sql);
                
                if (!b) {
                    JOptionPane.showMessageDialog(this, "Successfully Updated");
                    p_code.setText("");
                    p_name.setText("");
                    c_name.setText("");
                    c_location.setText("");
                    refresh_product_tbl_data();
                } else {
                    JOptionPane.showMessageDialog(this, "Error");
                }
                
                con.close();
                
            } catch (Exception e) {
                JOptionPane.showMessageDialog(this, "Error" + e.getMessage());
            }
        }
    }//GEN-LAST:event_edit_productActionPerformed

